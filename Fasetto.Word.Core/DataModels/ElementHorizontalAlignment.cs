namespace Fasetto.Word.Core
{
    /// <summary>
    /// horizontal alignment values for an element
    /// </summary>
    public enum ElementHorizontalAlignment
    {
        /// <summary>
        /// aligned to the left
        /// </summary>
        Left = 0,
        /// <summary>
        /// aligned to the center
        /// </summary>
        Center = 1,
        /// <summary>
        /// aligned to the right
        /// </summary>
        Right = 2,
        /// <summary>
        /// fills the width
        /// </summary>
        Stretch = 3
    }
}
