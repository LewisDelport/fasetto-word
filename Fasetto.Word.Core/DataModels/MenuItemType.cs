namespace Fasetto.Word.Core
{
    /// <summary>
    /// the types of items for a menu item
    /// </summary>
    public enum MenuItemType
    {
        /// <summary>
        /// shows the menu text and an icon
        /// </summary>
        TextAndIcon = 0,
        /// <summary>
        /// shows a simple divider between the menu items
        /// </summary>
        Divider = 1,
        /// <summary>
        /// shows the menu text as a header
        /// </summary>
        Header = 2,
    }
}
